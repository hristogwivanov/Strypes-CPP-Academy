#include<iostream>
#include<cmath>
#include<iomanip>
using namespace std;

int main(){
    float r;
    float h; 

    cout << "Даден е варел с формата на прав кръгов цилиндър с радиус на основата R метра и височина L метра. Варелът е пълен донякъде с вода." << endl;
    cout << "Варелът е в право състояние и е пълен с вода до височина H метра." << endl;
    cout << "Въведете от клавиатурата радиуса R и височината H за да получите обема на водата в кубически метри." << endl;

    do {
        cout << "Въведете радиуса R: ";
        cin >> r;
        if (r <= 0) {
            cout << "Грешка: Радиусът трябва да бъде положителен. Опитайте отново." << endl;
        }
    } while (r <= 0);

    do {
        cout << "Въведете височината H: ";
        cin >> h;
        if (h < 0) {
            cout << "Грешка: Височината не може да бъде отрицателна. Опитайте отново." << endl;
        }
    } while (h < 0);

    float area = M_PI * r * r;
    float volume = area * h;

    cout << fixed << setprecision(3);
    cout << "Обема на водата в кубически метри е " << volume << " кубически метра." << endl;

    return 0;
}
